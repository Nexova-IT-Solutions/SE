export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug);
}

export function getProductById(id: string): Product | undefined {
  return products.find(p => p.id === id);
}

export function getProductsByCategory(categoryId: string): Product[] {
  return products.filter(p => p.categoryId === categoryId);
}

export function getProductsByOccasion(occasionId: string): Product[] {
  return products.filter(p => p.occasionIds.includes(occasionId));
}

export function getFeaturedProducts(): Product[] {
  return products.filter(p => p.isFeatured);
}

export function getBestSellerProducts(): Product[] {
  return products.filter(p => p.isBestSeller);
}

export function getAddOnProducts(): Product[] {
  return products.filter(p => p.addOnCategory);
}
